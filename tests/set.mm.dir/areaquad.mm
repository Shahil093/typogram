theorem areaquad
  let vx: setvar x
  let vy: setvar y
  let cA: class A
  let cB: class B
  let cC: class C
  let cD: class D
  let cS: class S
  let cU: class U
  let cE: class E
  let cF: class F
  let cV: class V
  assume areaquad.1: |- A e. RR
  assume areaquad.2: |- B e. RR
  assume areaquad.3: |- C e. RR
  assume areaquad.4: |- D e. RR
  assume areaquad.5: |- E e. RR
  assume areaquad.6: |- F e. RR
  assume areaquad.7: |- A < B
  assume areaquad.8: |- C <_ E
  assume areaquad.9: |- D <_ F
  assume areaquad.10: |- U = ( C + ( ( ( x - A ) / ( B - A ) ) x. ( D - C ) ) )
  assume areaquad.11: |- V = ( E + ( ( ( x - A ) / ( B - A ) ) x. ( F - E ) ) )
  assume areaquad.12: |- S = { <. x , y >. | ( x e. ( A [,] B ) /\ y e. ( U [,] V ) ) }

  disjoint x y
  disjoint A x
  disjoint A y
  disjoint B x
  disjoint B y
  disjoint C x
  disjoint D x
  disjoint E x
  disjoint F x
  disjoint S x
  disjoint U y
  disjoint V y
  assert |- ( area ` S ) = ( ( ( ( F + E ) / 2 ) - ( ( D + C ) / 2 ) ) x. ( B - A ) )

