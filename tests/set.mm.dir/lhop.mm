  assume lhop.f: |- ( ph -> F : A --> RR )
  assume lhop.g: |- ( ph -> G : A --> RR )
  assume lhop.i: |- ( ph -> I e. ( topGen ` ran (,) ) )
  assume lhop.b: |- ( ph -> B e. I )
