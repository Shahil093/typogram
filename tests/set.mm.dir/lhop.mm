  assume lhop.ig: |- ( ph -> D C_ dom ( RR _D G ) )
  assume lhop.f0: |- ( ph -> 0 e. ( F limCC B ) )
  assume lhop.g0: |- ( ph -> 0 e. ( G limCC B ) )
  assume lhop.gn0: |- ( ph -> -. 0 e. ( G " D ) )
  assume lhop.gd0: |- ( ph -> -. 0 e. ( ( RR _D G ) " D ) )
  assume lhop.c: |- ( ph -> C e. ( ( z e. D |-> ( ( ( RR _D F ) ` z ) / ( ( RR _D G ) ` z ) ) ) limCC B ) )

