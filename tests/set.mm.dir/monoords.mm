  assume monoords.fk: |- ( ( ph /\ k e. ( M ... N ) ) -> ( F ` k ) e. RR )
  assume monoords.flt: |- ( ( ph /\ k e. ( M ..^ N ) ) -> ( F ` k ) < ( F ` ( k + 1 ) ) )
  assume monoords.i: |- ( ph -> I e. ( M ... N ) )
  assume monoords.j: |- ( ph -> J e. ( M ... N ) )
  assume monoords.iltj: |- ( ph -> I < J )

  disjoint F k
  disjoint I k
  disjoint J k
  disjoint M k
  disjoint N k
  disjoint k ph
  disjoint F j
  disjoint j k
  disjoint M j
  disjoint N j
  disjoint j ph
  assert |- ( ph -> ( F ` I ) < ( F ` J ) )

